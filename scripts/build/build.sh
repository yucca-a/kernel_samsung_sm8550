#!/usr/bin/env bash
# Main build entrypoint.
#
# One kernel image works on every Samsung SM8550 device (S23/Z5/Tab S9
# family). The AnyKernel3 zip lists all SM8550 codenames so a single
# zip flashes everywhere.
#
# Usage:
#   scripts/build/build.sh [mode]
#   mode : lkm | resukisu  (default: from .kernel_build_mode marker)
#
# Env overrides:
#   KERNEL_TAG                           : banner suffix (default YuccaA)
#   APPLY_SUSFS / APPLY_BBG / APPLY_ZRAM : 0|1, see apply_features.sh
#   JOBS                                 : parallel make jobs (default nproc)
#   SKIP_TOOLCHAIN_SETUP=1               : trust prebuilts/ as-is
#   ZIP_AFTER=0                          : skip auto-pack of AnyKernel3 zip

set -euo pipefail

# shellcheck source=common.sh
source "$(dirname -- "${BASH_SOURCE[0]}")/common.sh"

# Default mode comes from .kernel_build_mode (branch marker); fall back to lkm.
DEFAULT_MODE="lkm"
if [[ -f "${PROJECT_ROOT}/.kernel_build_mode" ]]; then
  DEFAULT_MODE="$(tr -d ' \n\r\t' < "${PROJECT_ROOT}/.kernel_build_mode")"
fi
MODE="${1:-${DEFAULT_MODE}}"
case "${MODE}" in
  lkm|resukisu) ;;
  *) die "unknown mode '${MODE}'. Use lkm or resukisu." ;;
esac

JOBS="${JOBS:-$(nproc)}"
OUT_DIR="${PROJECT_ROOT}/out/${MODE}"
mkdir -p "${OUT_DIR}"

log "Mode:    ${MODE}"
log "Tag:     ${KERNEL_TAG}"
log "Jobs:    ${JOBS}"
log "Out dir: ${OUT_DIR}"

# 1. Toolchain
# kernel_ci_center fetches our own clang-r450784e (yucca-a/sm8550-toolchain) and
# exports TOOLCHAIN_DIR; common.sh points CLANG_BIN into it, so setup_toolchain.sh
# sees clang already present and skips downloading. For standalone local builds
# (no TOOLCHAIN_DIR) it self-downloads r450784e into ${PROJECT_ROOT}/prebuilts.
if [[ "${SKIP_TOOLCHAIN_SETUP:-0}" != "1" ]]; then
  "${SCRIPT_DIR}/setup_toolchain.sh"
fi
export PATH="${CLANG_BIN}:${BUILDTOOLS_BIN}:${BUILDTOOLS_PATH_BIN}:${KERNEL_BUILD_TOOLS_BIN}:${PATH}"

# 2. ReSukiSU sources are always fetched: drivers/kernelsu is a symlink
#    that must resolve at Kconfig-parse time even when KSU is disabled.
#    Mode controls whether KSU is compiled into vmlinux (resukisu: y) or
#    entirely absent (lkm: n). In lkm mode the KSU manager app patches
#    init_boot at flash time to inject userspace KSU, which uses kprobes
#    to instrument the unmodified vmlinux at runtime.
"${SCRIPT_DIR}/fetch_kernelsu.sh"

# 2.5 Yucca ROM gate token.
#     The kernel image embeds SHA256(token) and verifies userspace knows
#     the token at boot. Token is generated once on first build and saved
#     to .yucca_token (never committed). The header is regenerated every
#     build from the current token so a token rotation only requires
#     editing .yucca_token and rebuilding -- not editing source.
TOKEN_FILE="${PROJECT_ROOT}/.yucca_token"
TOKEN_HEADER="${PROJECT_ROOT}/drivers/misc/yucca_gate_token.h"

if [[ ! -f "${TOKEN_FILE}" ]]; then
  log "Yucca gate: generating new token at ${TOKEN_FILE}..."
  openssl rand -hex 32 > "${TOKEN_FILE}"
  chmod 600 "${TOKEN_FILE}"
  warn "  KEEP ${TOKEN_FILE} SAFE. It is the only secret that lets"
  warn "  your ROM init.rc satisfy the kernel gate. Losing it means"
  warn "  every future ROM build is incompatible with old kernels."
fi

TOKEN=$(cat "${TOKEN_FILE}")
TOKEN_HASH_HEX=$(printf '%s' "${TOKEN}" | sha256sum | head -c 64)
TOKEN_HASH_BYTES=$(echo "${TOKEN_HASH_HEX}" | sed -E 's/(..)/0x\1, /g' | sed 's/, $//')

cat > "${TOKEN_HEADER}" <<EOF
/* Auto-generated by scripts/build/build.sh from .yucca_token. */
/* Do not edit, do not commit. */
#define YUCCA_EXPECTED_HASH_BYTES { ${TOKEN_HASH_BYTES} }
EOF

log "Yucca gate: token hash ${TOKEN_HASH_HEX:0:16}...${TOKEN_HASH_HEX: -16} burnt into header"

# 3. Optional features.
#    SuSFS is the one feature that MUST be off in lkm mode because its
#    fs/susfs.c references ksu_cred / ksu_is_manager() directly, which
#    only resolve when CONFIG_KSU=y. With KSU disabled in lkm mode those
#    symbols don't exist and vmlinux fails to link. Every other feature
#    (BBG / Unicode fix / NTSync / Droidspaces / BBR / Wild perf / zram)
#    is KSU-independent and is enabled in both modes.
if [[ "${MODE}" == "lkm" ]]; then
  APPLY_SUSFS=0 "${SCRIPT_DIR}/apply_features.sh"
else
  "${SCRIPT_DIR}/apply_features.sh"
fi

# 4. Configure
# Kernel localversion: match the sm8650/sm8750 abogki scheme
#   -android13-<kmi_gen>-<tag>-abogki<buildnum>-4k   (android13-5.15 == gen 5)
# A random 9-digit build number is generated each build; override BUILD_NUM
# to reproduce a specific release string.
BUILD_NUM="${BUILD_NUM:-$(shuf -i 100000000-999999999 -n 1)}"
LOCALVERSION="-${ANDROID_BASE}-${KMI_GENERATION}-${KERNEL_TAG}-abogki${BUILD_NUM}-${PAGE_SIZE_TAG}"
log "Localversion: ${LOCALVERSION}"
export KBUILD_BUILD_USER="universal"
export KBUILD_BUILD_HOST="sm8550"
export LOCALVERSION

# ccache: cache compiled objects so repeated builds with the same
# source content reuse cached .o files. Cuts iteration time by
# 60-80% on a typical kernel re-build. Set USE_CCACHE=0 to disable.
USE_CCACHE="${USE_CCACHE:-1}"
if [[ "${USE_CCACHE}" == "1" ]] && command -v ccache >/dev/null 2>&1; then
  export CCACHE_DIR="${CCACHE_DIR:-${HOME}/.ccache_kernel_sm8550}"
  mkdir -p "${CCACHE_DIR}"
  ccache --max-size=20G >/dev/null 2>&1 || true
  ccache --set-config=compression=true >/dev/null 2>&1 || true
  ccache --set-config=compression_level=1 >/dev/null 2>&1 || true
  CC_VAL='ccache clang'
  log "ccache enabled  (dir=${CCACHE_DIR})"
  ccache -s 2>/dev/null | grep -E "Hits|Misses|Cache size" | sed 's/^/  /' || true
else
  CC_VAL='clang'
  [[ "${USE_CCACHE}" == "1" ]] && warn "ccache requested but binary not found; using plain clang"
fi

MAKE_ARGS=(
  -C "${PROJECT_ROOT}"
  O="${OUT_DIR}"
  ARCH=arm64
  LLVM=1
  LLVM_IAS=1
  "CC=${CC_VAL}"
  # HOSTCC stays as plain clang -- wrapping the host compiler with
  # ccache while the build runs under nohup occasionally produces
  # "clang-14: error: unable to execute command: Hangup" when the
  # host-side linker (ld.lld for sorttable etc.) receives a stray
  # SIGHUP via the ccache wrapper. The target compiler (CC) does
  # not hit this because it runs further down the pipeline.
  HOSTCC=clang
  HOSTCXX=clang++
  LD=ld.lld
  AR=llvm-ar
  NM=llvm-nm
  OBJCOPY=llvm-objcopy
  OBJDUMP=llvm-objdump
  STRIP=llvm-strip
  CROSS_COMPILE=aarch64-linux-gnu-
  -j"${JOBS}"
)

log "Configuring (defconfig + gki_defconfig fragment)..."
make "${MAKE_ARGS[@]}" gki_defconfig

# LTO: use ThinLTO. The committed gki_defconfig ships CONFIG_LTO_CLANG_FULL=y +
# CONFIG_CFI_CLANG=y. FULL LTO OOM-kills the 16 GB CI runner (exit 143), but
# turning LTO fully OFF also silently drops CFI (CONFIG_CFI_CLANG depends on
# LTO_CLANG) and produces a NON-BOOTING image. ThinLTO has far lower peak
# memory than FULL (fits the runner) AND keeps CFI enabled, matching Samsung's
# hardening -- the AOSP/Pixel-standard combination, and it boots.
log "Selecting ThinLTO (keeps CFI, fits CI memory; FULL LTO OOMs)..."
scripts/config --file "${OUT_DIR}/.config" \
  -d LTO_NONE -e LTO_CLANG -e LTO_CLANG_THIN -d LTO_CLANG_FULL

# Add NTFS3 (read/write NTFS, e.g. OTG drives) -- the sm8650/sm8750 trees ship
# it and we lacked it. We deliberately do NOT disable HUGEPAGE_POOL (unlike
# those trees): Samsung's 5.15 mm/kzerod.c only references need_pause() and
# kzerod_app_launch_nb under CONFIG_HUGEPAGE_POOL=y, and the tree builds with
# -Werror, so turning it off makes them unused -> -Wunused-function/-variable
# error. Keep HUGEPAGE_POOL on for sm8550 (it boots fine with it on).
log "Enabling NTFS3 + full tmpfs + TTL target (keeping HUGEPAGE_POOL on; Samsung mm needs it under -Werror)..."
scripts/config --file "${OUT_DIR}/.config" \
  -e NTFS3_FS -e NTFS3_LZX_XPRESS \
  -e TMPFS -e TMPFS_POSIX_ACL -e TMPFS_XATTR -e TMPFS_INODE64 \
  -e NETFILTER_XT_TARGET_HL

# Mode feature configs, mirroring the sm8650/sm8750 trees:
#   resukisu = KSU + SUSFS built in
#   lkm      = pure kernel, none of them (KSU injected at flash time by the
#              manager app patching init_boot; SUSFS must be off).
# Enabling KSU_SUSFS is what actually turns SuSFS on (apply_features.sh only
# drops in the source + Kconfig). KPM was dropped: ReSukiSU upstream removed it
# (PR #226, 2026-06-06), so there is no kernel-side KPM driver to enable.
if [[ "${MODE}" == "lkm" ]]; then
  log "lkm: disabling KSU / KSU_SUSFS (pure kernel; KSU injected at flash time)..."
  scripts/config --file "${OUT_DIR}/.config" --disable KSU --disable KSU_SUSFS
else
  log "resukisu: enabling KSU / KSU_SUSFS..."
  scripts/config --file "${OUT_DIR}/.config" --enable KSU --enable KSU_SUSFS
fi

make "${MAKE_ARGS[@]}" olddefconfig

# 5. Build with auto-retry. WSL2 ext4 occasionally fails with
#    'clang: unable to rename .o.tmp to .o: No such file or directory'
#    when many parallel mkdirs race during the first build. Each retry
#    creates more directories, so 2-4 retries usually converge. The
#    retry is skipped if the make failure mentions any non-rename
#    error (real compile / link issue).
log "Building kernel Image (with auto-retry on rename races)..."
IMAGE="${OUT_DIR}/arch/arm64/boot/Image"
MAX_RETRY=5
attempt=0
while true; do
  attempt=$((attempt + 1))
  build_log="${OUT_DIR}/.make.attempt${attempt}.log"
  # Delete any prior Image so a stale file (from an earlier build or
  # a prior attempt that died after writing Image) cannot masquerade
  # as success in the post-failure check below. If make succeeds this
  # attempt, Image reappears via OBJCOPY in a fraction of a second.
  rm -f "${IMAGE}"
  set +o pipefail
  make "${MAKE_ARGS[@]}" Image 2>&1 | tee "${build_log}"
  rc=${PIPESTATUS[0]}
  set -o pipefail
  if [[ ${rc} -eq 0 ]]; then
    log "make Image succeeded on attempt ${attempt}."
    break
  fi
  # WSL2 rename race sometimes triggers during the final OBJCOPY step,
  # after Image has actually been written. Accept that as success since
  # the artifact we want is on disk and Image is newer than the rm -f
  # above (so it is guaranteed to have been produced this attempt).
  if [[ -f "${IMAGE}" ]]; then
    log "Image produced on attempt ${attempt} despite reported failure (likely rename race)."
    break
  fi
  if [[ ${attempt} -ge ${MAX_RETRY} ]]; then
    die "make Image failed after ${MAX_RETRY} attempts; see ${build_log}"
  fi
  # Bail early on real compile/link errors (anything but the WSL2 rename
  # race). If we see at least one non-rename 'error:' line, abort.
  real_errs=$(grep -E '^.*error:' "${build_log}" \
              | grep -vc 'error: unable to rename temporary' || true)
  if (( real_errs > 0 )); then
    die "make Image hit a real compile/link error (${real_errs} lines); see ${build_log}"
  fi
  warn "Attempt ${attempt}: failure with no real compile error; retrying..."
done

if [[ ! -f "${IMAGE}" ]]; then
  die "Image not produced at ${IMAGE}"
fi
ok "Build succeeded: ${IMAGE}"
ls -lh "${IMAGE}"

# 6.5 KPM: dropped. ReSukiSU upstream removed KPM support entirely (PR #226,
# merged 2026-06-06). drivers/kernelsu is fetched from ReSukiSU @ main, so the
# kernel-side KPM driver is gone; running patch_linux would only stamp an inert
# patch onto the Image (and falsely log "KPM-patched"). resukisu mode now ships
# KSU + SUSFS only. To bring KPM back, point fetch_kernelsu.sh at a KPM-capable
# source (e.g. SukiSU-Ultra) and restore the patch_linux step here.

# 7. Optional: chain into AnyKernel3 packaging. Default on.
if [[ "${ZIP_AFTER:-1}" == "1" ]]; then
  log "Packing AnyKernel3 zip..."
  "${SCRIPT_DIR}/pack_anykernel.sh" "${MODE}"
else
  log "Skipping zip pack (ZIP_AFTER=0). Run scripts/build/pack_anykernel.sh ${MODE} when ready."
fi
