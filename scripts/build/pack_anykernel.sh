#!/usr/bin/env bash
# Package the compiled Image into a single AnyKernel3 flashable zip
# that targets every SM8550 device (S23 / Z Fold5 / Z Flip5 / Tab S9).
#
# Usage:
#   scripts/build/pack_anykernel.sh [mode]
#   mode : lkm | resukisu  (default: lkm)
#
# Reads  ${PROJECT_ROOT}/out/<mode>/arch/arm64/boot/Image
# Writes ${PROJECT_ROOT}/out/<mode>/SM8550_<tag>_<base-version>_<MMDD>.zip

set -euo pipefail

# shellcheck source=common.sh
source "$(dirname -- "${BASH_SOURCE[0]}")/common.sh"

MODE="${1:-lkm}"
case "${MODE}" in
  lkm|resukisu) ;;
  *) die "unknown mode '${MODE}'. Use lkm or resukisu." ;;
esac

OUT_DIR="${PROJECT_ROOT}/out/${MODE}"
IMAGE="${OUT_DIR}/arch/arm64/boot/Image"
[[ -f "${IMAGE}" ]] || die "Image not found at ${IMAGE} -- run build.sh first."

# Zip scheme (unified across sm8550/sm8650/sm8750): <prefix>_<tag>_<base-version>_<MMDD>.zip
# e.g. SM8550_resukisu_5.15.207_0602.zip / SM8550_LKM_5.15.207_0602.zip. tag = resukisu/LKM;
# base-version is VERSION.PATCHLEVEL.SUBLEVEL (kernel.release with the localversion
# stripped); MMDD is the build month+day. The full -android13-5-...-abogki<n>-4k
# string still lives in uname.
TAG="resukisu"; [[ "${MODE}" == "lkm" ]] && TAG="LKM"
MD="$(date +%m%d)"
REL_FILE="${OUT_DIR}/include/config/kernel.release"
if [[ -f "${REL_FILE}" ]]; then
  BASE_VER="$(cut -d- -f1 < "${REL_FILE}")"
  ZIP_NAME="${ZIP_PREFIX}_${TAG}_${BASE_VER}_${MD}.zip"
else
  ZIP_NAME="${ZIP_PREFIX}_${TAG}_${MD}.zip"
fi
ZIP_PATH="${OUT_DIR}/${ZIP_NAME}"

# AnyKernel3 base tree: kernel_ci_center clones one and exports ANYKERNEL_DIR;
# standalone local builds fall back to a committed anykernel3/ in the repo.
# Either way we render our own anykernel.sh (SM8550 device list) into it below.
AK3_BASE="${ANYKERNEL_DIR:-${PROJECT_ROOT}/anykernel3}"
[[ -d "${AK3_BASE}" ]] || die "AnyKernel3 tree not found at ${AK3_BASE} -- set ANYKERNEL_DIR or add anykernel3/ to the repo."

WORK="${OUT_DIR}/anykernel_pack"
rm -rf "${WORK}"
log "Staging AnyKernel3 tree from ${AK3_BASE} at ${WORK}..."
cp -a "${AK3_BASE}" "${WORK}"
# Systemless boot-only flash; drop the upstream sample ramdisk/patch.
rm -rf "${WORK}/ramdisk" "${WORK}/patch" "${WORK}/modules"
mkdir -p "${WORK}/ramdisk" "${WORK}/patch" "${WORK}/modules"
cp -p "${IMAGE}" "${WORK}/Image"

# Render all SM8550 codenames into device.nameN lines.
DEVICE_LINES=""
i=1
for codename in "${SM8550_CODENAMES[@]}"; do
  DEVICE_LINES+="device.name${i}=${codename}"$'\n'
  i=$((i + 1))
done

KERNEL_STRING="Universal SM8550 (${MODE}) - ${KERNEL_TAG}"

cat > "${WORK}/anykernel.sh" <<AK
### AnyKernel3 mod script for kernel_samsung_sm8550
## one Image, every SM8550 device
## generated by scripts/build/pack_anykernel.sh

properties() { '
kernel.string=${KERNEL_STRING}
do.devicecheck=1
do.modules=0
do.systemless=1
do.cleanup=1
do.cleanuponabort=0
${DEVICE_LINES}supported.versions=
supported.patchlevels=
supported.vendorpatchlevels=
'; } # end properties

BLOCK=/dev/block/by-name/boot;
IS_SLOT_DEVICE=0;
RAMDISK_COMPRESSION=auto;
PATCH_VBMETA_FLAG=auto;

. tools/ak3-core.sh;

# Systemless: only replace the kernel image inside boot, do not touch the
# ramdisk (Samsung GKI keeps the userspace ramdisk in init_boot).
split_boot;
flash_boot;
AK

log "Building zip ${ZIP_PATH}..."
( cd "${WORK}" && rm -f "${ZIP_PATH}" && \
    zip -r9 "${ZIP_PATH}" . -x ".git*" "*.zip" >/dev/null )

ok "Wrote $(du -h "${ZIP_PATH}" | cut -f1) -> ${ZIP_PATH}"
ls -lh "${ZIP_PATH}"
log "Compatible codenames: ${SM8550_CODENAMES[*]}"

# When invoked by kernel_ci_center (it exports PACK=1), also drop the zip in
# the kernel-repo parent dir, which is where the CI collects build artifacts
# (matches the sm8650/sm8750 build.sh convention of writing to KROOT/..).
if [[ "${PACK:-0}" == "1" ]]; then
  CI_ARTIFACT="${PROJECT_ROOT}/../${ZIP_NAME}"
  cp -f "${ZIP_PATH}" "${CI_ARTIFACT}"
  ok "Copied to CI artifact dir: ${CI_ARTIFACT}"
fi
